<template>
  <div id="login" class="page">
    <div id="login-content">
      <h1>
        Log in to <span id="mangadex"><strong>MangaDex</strong></span>
      </h1>
      <label class="input-text">
        <input v-model="username" type="text" required />
        <span>Username/Email</span>
      </label>
      <label class="input-text">
        <input v-model="password" type="password" required />
        <span>Password</span>
      </label>
      <p id="error">{{ errorMessage }}</p>
      <button @click="login">Log In</button>
    </div>
  </div>
</template>

<script lang="ts">
import { Vue } from "vue-class-component";
import { store } from "@/store";
import router from "@/router";
import { AxiosError, AxiosResponse } from "axios";
import { login } from "@/ts/network/calls";
import { LoginResponse, ErrorResponse } from "@/ts/model/response";
import { handleErrorMessage } from "@/ts/util/errorMessage";

export default class Login extends Vue {
  private username = "";
  private password = "";
  private errorMessage = "";

  private login() {
    // Validate username and checks if email
    if (this.username.length === 0) {
      this.errorMessage = "Please enter your username";
      return;
    }

    var isEmail = false;
    if (this.username.includes("@")) {
      isEmail = true;
    }

    // Validates password
    if (this.password.length === 0) {
      this.errorMessage = "Please enter your password";
      return;
    } else if (this.password.length < 8) {
      this.errorMessage = "Password has to have at least 8 characters";
      return;
    }

    // Call MangaDex API
    login(this.username, this.password, isEmail)
      .then((response: AxiosResponse<LoginResponse>) => {
        store.commit("setToken", response.data.token);
        router.push("loading");
      })
      .catch((error: AxiosError<ErrorResponse>) => {
        this.errorMessage = handleErrorMessage(error);
      });
  }
}
</script>
